class Calculator:
    """计算器类
    提供基本数学运算
    """
    
    def add(self, a, b):
        """加法函数
        返回两数之和
        """
        # 执行加法
        return a + b
    
    def subtract(self, a, b):
        '''减法函数
        返回两数之差
        '''
        return a - b
    
    def multiply(self, a, b):
        """乘法"""
        return a * b


